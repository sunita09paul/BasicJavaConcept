package LearnJava;

public class Switch_case {

	public static void main(String[] args) {
		
		String day ="mon1";
		switch (day){
			case "mon" : System.out.println("MOnday");
			break;
			case "tue" : System.out.println("Tuesday");
			break;
			default : System.out.println("invalid day");
		}

	}

}
